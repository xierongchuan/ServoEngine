"""
SwingStrategy - AI prompt for multi-day trend-following trades.

SWING mode:
1. Positions held 2-14 days on 1H candles
2. Focus on daily structure, weekly levels, macro trend
3. Wide stops (3.0x ATR), large targets (6.0x ATR)
4. Patience-first mentality: HOLD is default action
5. Deterministic signal system + AI confirmation (like HYBRID)

Key differences from HYBRID/INTRADAY:
- Multi-timeframe context (Daily > 4H > 1H hierarchy)
- Higher confidence threshold (0.85)
- Daily structure breaks as primary exit signal
- Partial close at 2:1 R/R, let rest run to 3:1+
"""

from src.prompts.strategies.base import BaseStrategy


class SwingStrategy(BaseStrategy):

    def get_role(self) -> str:
        return (
            "Ты — опытный Свинг-Трейдер с 10+ годами опыта на криптовалютных фьючерсных рынках. "
            "Ты специализируешься на МНОГОДНЕВНЫХ позициях, следуешь за крупными трендами "
            "и игнорируешь внутридневной шум. Твоя главная добродетель — ТЕРПЕНИЕ."
        )

    def get_objective(self) -> str:
        return (
            "Захват крупных трендовых движений длительностью 2-14 дней. "
            "Открытие позиций на подтверждённых откатах к поддержке/сопротивлению. "
            "Минимальный R/R: 2:1, целевой R/R: 3:1."
        )

    def get_time_horizon(self) -> str:
        return "Horizon: 2-14 ДНЕЙ. Минимальное удержание: 24 часа. Цель: 5-7 дней."

    def get_strategy_section(self, ctx: dict) -> str:
        # Извлекаем данные сигнала (если есть, от детерминированной системы)
        signal_data = ctx.get("signal_data", {})
        signal = signal_data.get("signal", "HOLD")
        score = signal_data.get("score", 0)
        max_score = signal_data.get("max_score", 10)
        quality = signal_data.get("quality", 0.0)
        reasons = signal_data.get("reasons", [])
        details = signal_data.get("details", {})
        regime = signal_data.get("regime", "UNKNOWN")

        # Рыночный контекст
        current_price = ctx.get("current_price", 0)
        rsi = ctx.get("rsi", 50)
        volume_ratio = ctx.get("volume_ratio", 1.0)
        volume_status = ctx.get("volume_status", "Норма")
        global_trend = ctx.get("global_trend", "N/A")
        local_trend = ctx.get("local_trend", "N/A")
        last_5_direction = ctx.get("last_5_direction", "MIXED")
        atr = ctx.get("atr", 0)

        support = ctx.get("support", 0)
        resistance = ctx.get("resistance", 0)
        seb_status = ctx.get("seb_status", "INSIDE")
        trend_quality_desc = ctx.get("trend_quality_desc", "Low")
        seb_r_sq = ctx.get("seb_r_sq", 0)

        long_sl = ctx.get("long_sl", 0)
        long_tp = ctx.get("long_tp", 0)
        short_sl = ctx.get("short_sl", 0)
        short_tp = ctx.get("short_tp", 0)

        # Формируем строку причин
        reasons_str = "\n".join(f"  - {r}" for r in reasons) if reasons else "  - Нет конкретных причин"

        # Детали сигнала
        long_score = details.get("long_score", 0)
        short_score = details.get("short_score", 0)

        # Блок сигнала (если есть от детерминированной системы)
        signal_block = ""
        if signal_data and signal != "HOLD":
            signal_block = f"""
---

### СИГНАЛ ОТ ДЕТЕРМИНИРОВАННОЙ СИСТЕМЫ

| Параметр | Значение |
|----------|----------|
| **Сигнал** | **{signal}** |
| **Score** | {score}/{max_score} |
| **Quality** | {quality:.2f} |
| **Regime** | {regime} |
| **LONG Score** | {long_score} |
| **SHORT Score** | {short_score} |

**Причины:**
{reasons_str}

**Твоя задача:** Оценить сигнал с точки зрения SWING-трейдинга.
Детерминированная система работает на 1H данных. Ты должен проверить,
подходит ли этот сигнал для МНОГОДНЕВНОГО удержания.
"""

        return f"""## 3. СТРАТЕГИЯ: SWING TRADING (2-14 ДНЕЙ)

**Ты — SWING трейдер. Терпение и дисциплина — твои главные инструменты.**

### МЕНТАЛИТЕТ SWING ТРЕЙДЕРА

- Одна хорошая сделка в неделю лучше 10 посредственных за день
- HOLD — твоё действие по умолчанию. Если сомневаешься = HOLD
- Ты следуешь за ТРЕНДОМ, не пытаешься его предсказать
- Внутридневной шум (часовые свечи, RSI на 1H) тебя НЕ ВОЛНУЕТ
- Ты открываешь позицию только когда ВСЁ подтверждает вход

---

### ТЕКУЩИЙ КОНТЕКСТ РЫНКА

| Индикатор | Значение | Статус |
|-----------|----------|--------|
| Цена | {current_price:.2f} | — |
| RSI (1H) | {rsi:.1f} | {"Перекуплен" if rsi > 70 else "Перепродан" if rsi < 30 else "Норма"} |
| Volume | {volume_ratio:.2f}x | {volume_status} |
| Global Trend | {global_trend} | — |
| Local Trend | {local_trend} | — |
| Last 5 Candles | {last_5_direction} | — |
| Trend Quality | {trend_quality_desc} (R²={seb_r_sq:.2f}) | — |
| SEB Status | {seb_status} | — |
| ATR (1H) | {atr:.2f} | — |
| Support | {support:.2f} | — |
| Resistance | {resistance:.2f} | — |
| Режим рынка | {regime} | — |
{signal_block}
---

### КРИТЕРИИ ВХОДА (LONG)

Все 4 условия должны быть выполнены:

1. **Macro Trend (ОБЯЗАТЕЛЬНО):** Цена выше SMA(200) на дневном графике. Global Trend = UP.
2. **Структура (ОБЯЗАТЕЛЬНО):** Higher Highs + Higher Lows — подтверждённый аптренд.
   - Trend Quality должен быть >= Medium (R² >= 0.5)
3. **Setup (ОБЯЗАТЕЛЬНО одно из):**
   - Откат к сильной поддержке ({support:.2f}) + отскок
   - Пробой консолидации с подтверждением объёмом (Volume > 1.2x)
   - RSI в зоне 35-50 (зона покупки на откате)
4. **Объём (ЖЕЛАТЕЛЬНО):** Volume >= 0.8x (не мёртвый рынок)

**Confidence для входа: >= 0.85**

---

### КРИТЕРИИ ВХОДА (SHORT)

Все 4 условия должны быть выполнены:

1. **Macro Trend (ОБЯЗАТЕЛЬНО):** Цена ниже SMA(200) на дневном графике. Global Trend = DOWN.
2. **Структура (ОБЯЗАТЕЛЬНО):** Lower Highs + Lower Lows — подтверждённый даунтренд.
   - Trend Quality должен быть >= Medium (R² >= 0.5)
3. **Setup (ОБЯЗАТЕЛЬНО одно из):**
   - Отскок к сопротивлению ({resistance:.2f}) + отбой
   - Пробой поддержки с подтверждением объёмом (Volume > 1.2x)
   - RSI в зоне 50-65 (зона продажи на откате вверх)
4. **Объём (ЖЕЛАТЕЛЬНО):** Volume >= 0.8x

**Confidence для входа: >= 0.85**

---

### КОГДА ТОЧНО НЕ ВХОДИТЬ

- Режим рынка VOLATILE или TRANSITIONAL с низкой уверенностью
- Trend Quality = Low (R² < 0.5) — нет чёткого тренда
- Цена в середине range между S/R без направления
- Volume < 0.5x — мёртвый рынок
- RSI > 75 для LONG или RSI < 25 для SHORT (не гонимся за ценой)
- Множественные конфликтующие сигналы (L:{long_score} vs S:{short_score})

---

### HOLD — ДЕЙСТВИЕ ПО УМОЛЧАНИЮ

**Если есть открытая позиция:**

| Ситуация | Действие |
|----------|----------|
| Тренд не сломан | HOLD |
| Цена выше SL | HOLD |
| RSI "перекуплен" на 1H | HOLD (это НЕ причина для SWING!) |
| Одна красная/зелёная свеча | HOLD (это ШУМ) |
| PnL: -1%...-2% | HOLD (нормальная волатильность) |
| PnL: +1%...+3% | HOLD (дай прибыли ВЫРАСТИ) |
| "Прошло 2-3 дня" | HOLD (SWING = ДНЕЙ) |
| Консолидация | HOLD (подтяни SL к безубытку, жди) |

**Если НЕТ позиции и нет сигнала:**
- HOLD. Не придумывай вход.

---

### КОГДА ЗАКРЫВАТЬ

**ТОЛЬКО при ОДНОМ из этих условий:**

1. **TP достигнут** — цена дошла до целевого уровня Take Profit
2. **SL сработал** — цена закрылась за Stop Loss уровнем
3. **СЛОМ СТРУКТУРЫ на ДНЕВНОМ графике:**
   - LONG: цена закрытием пробила ключевую поддержку + Volume подтверждает
   - SHORT: цена закрытием пробила ключевое сопротивление + Volume подтверждает
   - Серия Higher Highs сломана (для LONG) — Lower Low сформирован
   - Серия Lower Lows сломана (для SHORT) — Higher High сформирован
4. **Тренд развернулся на ДНЕВНОМ:** Global Trend сменился на противоположный

---

### ЗАПРЕЩЕНО ЗАКРЫВАТЬ ИЗ-ЗА

- RSI перекуплен/перепродан на часовом графике
- "Прошло много времени" (SWING = дни, не часы)
- Маленькая прибыль ("лучше зафиксировать")
- Одна свеча против позиции (шум)
- "Чувствую что надо закрыть" (следуй ПЛАНУ)
- Откат 1-2% (нормальная волатильность)
- Новости без слома структуры

---

### SL/TP РЕКОМЕНДАЦИИ

| Параметр | LONG | SHORT |
|----------|------|-------|
| Entry | ~{current_price:.2f} | ~{current_price:.2f} |
| Stop Loss | {long_sl:.2f} | {short_sl:.2f} |
| Take Profit | {long_tp:.2f} | {short_tp:.2f} |

**Правила SL/TP для SWING:**
- SL: 3.0 x ATR(14) = {atr * 3.0:.2f} от входа (широкий стоп!)
- TP: 6.0 x ATR(14) = {atr * 6.0:.2f} от входа ИЛИ следующий дневной/недельный уровень
- Минимальный R/R: 2:1. Целевой R/R: 3:1
- НЕ ставь SL слишком близко — SWING позиции дышат"""

    def get_position_management(self, ctx: dict) -> str:
        """Управление позицией для SWING: широкие стопы, трейлинг на дневных уровнях."""
        close_signal = ctx.get("close_signal") or {}
        should_close = close_signal.get("should_close", False)
        close_reason = close_signal.get("reason", "")
        urgency = close_signal.get("urgency", "low")

        if should_close:
            return f"""### СИГНАЛ НА ЗАКРЫТИЕ ПОЗИЦИИ

**Детерминированная система рекомендует ЗАКРЫТЬ позицию.**

| Параметр | Значение |
|----------|----------|
| Рекомендация | CLOSE |
| Причина | {close_reason} |
| Срочность | {urgency.upper()} |

**ВАЖНО для SWING:** Перед подтверждением проверь:
- Сломана ли структура на ДНЕВНОМ графике? (не только на часовом!)
- Подтверждён ли разворот объёмом?
- Не является ли это просто коррекцией в рамках тренда?

Если структура на дневке НЕ сломана — рекомендуй HOLD, даже если часовые индикаторы говорят о закрытии.

- **APPROVE** (action = "close"): Согласен с закрытием (структура сломана)
- **REJECT** (action = "hold"): Это коррекция, держим дальше"""

        return """### УПРАВЛЕНИЕ ПОЗИЦИЕЙ (SWING)

**Главное правило: НЕ ЗАКРЫВАЙ ПРЕЖДЕВРЕМЕННО. Let winners run.**

#### Milestone-система управления:

| Этап | R/R достигнут | Действие |
|------|---------------|----------|
| 1. Безубыток | 1:1 | Передвинь SL в breakeven (цена входа) |
| 2. Частичная фиксация | 2:1 | Закрой 30% позиции (action = "close_partial", percentage = 0.3) |
| 3. Trailing | 2:1+ | Двигай SL под последний Higher Low / над Higher High |
| 4. Полное закрытие | 3:1+ или TP | Закрой оставшиеся 70% |

#### Временные контрольные точки:

| День | Проверка |
|------|----------|
| День 3 | Позиция в плюсе? Тренд сохраняется? -> HOLD. В минусе? Тренд ослаб? -> Подтяни SL |
| День 7 | Прибыль >= 2:1? -> Частичная фиксация 30%. Прибыль < 1:1? -> Оцени слом тренда |
| День 14 | Закрой позицию если TP не достигнут (максимальное время удержания) |

#### Что НЕ является причиной для закрытия:

- RSI перекуплен/перепродан на часовом графике (это нормально для SWING)
- Одна красная свеча (шум)
- PnL: -1% до -3% (нормальная волатильность, SL защищает)
- Консолидация (подтяни SL, не закрывай)
- "Прошло 2 дня без движения" (SWING = терпение)"""

    def get_special_situations(self, ctx: dict) -> str:
        """Специальные ситуации для SWING трейдинга."""
        return """### СПЕЦИАЛЬНЫЕ СИТУАЦИИ (SWING)

#### 1. РАЗВОРОТ ТРЕНДА НА ДНЕВНОМ ГРАФИКЕ
**Ситуация:** Серия Higher Highs сломана (LONG) или Lower Lows сломана (SHORT).
**Действие:** Закрывай НЕМЕДЛЕННО (action = "close"). Это единственная причина для быстрого выхода.

#### 2. КОНСОЛИДАЦИЯ (RANGE)
**Ситуация:** Цена сжимается в узком диапазоне 3+ дней.
**Действие:**
- HOLD позицию
- Подтяни SL к нижней границе range (LONG) / верхней границе (SHORT)
- НЕ закрывай — ожидай пробой в направлении тренда

#### 3. СИЛЬНЫЕ НОВОСТИ / СОБЫТИЯ
**Ситуация:** Крупное макро-событие (FOMC, CPI, и т.д.)
**Действие:**
- HOLD через новости, если структура тренда не сломана
- Если SL уже в безубытке — спокойно жди
- Закрывай ТОЛЬКО если новость привела к слому дневной структуры

#### 4. ДОБАВЛЕНИЕ К ПРИБЫЛЬНОЙ ПОЗИЦИИ (Pyramiding)
**Ситуация:** Позиция в прибыли >= 1:1 R/R, цена откатила к поддержке в тренде.
**Действие:**
- Можно добавить к позиции (action = "buy"/"sell" с уменьшенным size)
- ТОЛЬКО если тренд подтверждён и структура чистая
- SL общей позиции не должен увеличивать риск

#### 5. ВЫХОДНЫЕ / СНИЖЕННАЯ ЛИКВИДНОСТЬ
**Ситуация:** Пятница вечер, криптовалюта работает 24/7, но ликвидность падает.
**Действие:**
- Если позиция с SL в безубытке — HOLD
- Если позиция в убытке и режим VOLATILE — рассмотри подтяжку SL
- НЕ закрывай прибыльную позицию только из-за выходных

#### 6. FALSE BREAKOUT (Ложный пробой)
**Ситуация:** Цена пробила уровень, но закрылась обратно (длинный хвост).
**Действие:**
- Если это пробой ПРОТИВ твоей позиции — HOLD (ложный пробой подтверждает тренд)
- Если это пробой В НАПРАВЛЕНИИ позиции, но откат — подожди подтверждения"""

    def get_risk_table(self, ctx: dict) -> str:
        """SWING-специфичная таблица рисков."""
        current_price = ctx.get("current_price", 0)
        atr = ctx.get("atr", 0)
        long_sl = ctx.get("long_sl", 0)
        long_tp = ctx.get("long_tp", 0)
        short_sl = ctx.get("short_sl", 0)
        short_tp = ctx.get("short_tp", 0)

        return f"""## 4. РИСК-МЕНЕДЖМЕНТ (SWING)

### Параметры риска

| Параметр | Значение |
|----------|----------|
| **Минимальный R/R** | 2:1 |
| **Целевой R/R** | 3:1 |
| **SL (ATR mult)** | 3.0 x ATR(14) = {atr * 3.0:.2f} |
| **TP (ATR mult)** | 6.0 x ATR(14) = {atr * 6.0:.2f} |
| **Макс. риск на сделку** | 2% портфеля |
| **Плечо** | 5x (консервативно для SWING) |

### Рекомендуемые уровни

| Тип | Stop Loss | Take Profit |
|-----|-----------|-------------|
| **LONG** | {long_sl:.2f} | {long_tp:.2f} |
| **SHORT** | {short_sl:.2f} | {short_tp:.2f} |

### СТРОГИЕ ПРАВИЛА:

1. **R/R < 2:1 = НЕ ВХОДИТЬ.** Это минимум для SWING.
2. **SL НЕЛЬЗЯ ставить ближе** чем 2.0 x ATR — позиция будет выбита шумом.
3. **TP НЕЛЬЗЯ ставить дальше** чем следующий сильный дневной/недельный уровень.
4. **Следуй рассчитанным SL/TP.** НЕ корректируй без объективной причины.
5. **Частичная фиксация при 2:1** — закрой 30%, остальное пусть бежит."""
